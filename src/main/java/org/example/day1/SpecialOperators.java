package org.example.day1;

public class SpecialOperators {
    public static void main(String[] args) {
        // modulo
        int x = 45 % 6; // 3

        // unary operators - verschil tussen pre en postfix
        int y = 1;
        int z = y++; // z = 1, y = 2

        int a = 1;
        int b = ++a; // b = 2, a = 2

        int c = 1;
        int d = 2;
        int e = c++ * ++d;


    }
}
